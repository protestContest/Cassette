i32 GetParseAction(i32 state, u32 sym)
{
  if (actions[state] < 0) return -1;
  return parse_table[actions[state]+sym];
}

i32 GetParseGoto(i32 state, u32 sym)
{
  if (gotos[state] < 0) return -1;
  PrintInt(parse_table[gotos[state]+sym]);
  return parse_table[gotos[state]+sym];
}

i32 GetParseReduction(i32 state)
{
  return reductions[state];
}

i32 GetReductionNum(i32 state)
{
  return reduction_sizes[state];
}

i32 IsLiteral(i32 sym)
{
  for (u32 i = 0; i < ArrayCount(literals); i++) {
    if (literals[i] == sym) {
      return true;
    }
  }
  return false;
}

char *ParseSymbolName(i32 sym)
{
  return parse_symbols[sym];
}
